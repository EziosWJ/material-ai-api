CREATE TABLE IF NOT EXISTS biz_material (
    id BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键 ID',
    user_id BIGINT NOT NULL COMMENT '所属用户 ID',
    title VARCHAR(200) NOT NULL COMMENT '材料标题',
    original_filename VARCHAR(255) NOT NULL COMMENT '原始文件名快照',
    file_id BIGINT NOT NULL COMMENT '关联 sys_file 文件记录 ID',
    file_type VARCHAR(50) NULL COMMENT '文件类型，如 pdf、docx、txt',
    file_size BIGINT NOT NULL DEFAULT 0 COMMENT '文件大小，单位字节',
    file_md5 VARCHAR(32) NULL COMMENT '文件 MD5 快照',
    storage_path VARCHAR(500) NOT NULL COMMENT '文件存储路径快照',
    status VARCHAR(32) NOT NULL DEFAULT 'processing' COMMENT '材料状态：processing、available、failed',
    segment_count INT NOT NULL DEFAULT 0 COMMENT '当前可用片段数量',
    last_process_time DATETIME NULL COMMENT '最近处理完成时间',
    error_message TEXT NULL COMMENT '最近一次处理失败原因',
    remark VARCHAR(500) NULL COMMENT '备注',
    create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    create_by BIGINT NULL COMMENT '创建人 ID',
    update_by BIGINT NULL COMMENT '更新人 ID',
    deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除：0=正常，1=已删除',
    PRIMARY KEY (id),
    KEY idx_biz_material_user_id (user_id),
    KEY idx_biz_material_file_id (file_id),
    KEY idx_biz_material_status (status),
    KEY idx_biz_material_create_time (create_time),
    KEY idx_biz_material_deleted (deleted)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='材料主表';

