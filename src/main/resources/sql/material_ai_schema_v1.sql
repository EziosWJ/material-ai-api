CREATE TABLE IF NOT EXISTS biz_ai_call_log (
    id BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键 ID',
    user_id BIGINT NOT NULL COMMENT '调用用户 ID',
    business_type VARCHAR(50) NOT NULL COMMENT '业务类型：material_process、material_vector_delete、writing、qa',
    business_id BIGINT NULL COMMENT '业务 ID，如材料 ID、任务 ID 或会话 ID',
    endpoint VARCHAR(255) NOT NULL COMMENT 'Python 服务接口路径',
    model_name VARCHAR(100) NULL COMMENT '模型名称',
    material_ids_json TEXT NULL COMMENT '本次调用涉及的材料 ID 快照 JSON 字符串',
    request_summary TEXT NULL COMMENT '请求摘要，不保存完整请求体',
    response_summary TEXT NULL COMMENT '响应摘要，不保存完整响应体',
    status VARCHAR(32) NOT NULL COMMENT '调用状态：success、failed',
    http_status INT NULL COMMENT 'Python HTTP 状态码',
    error_code VARCHAR(100) NULL COMMENT 'Python 错误码',
    error_message TEXT NULL COMMENT '错误信息',
    source_count INT NOT NULL DEFAULT 0 COMMENT '来源片段数量',
    trace_id VARCHAR(100) NULL COMMENT '调用链追踪 ID',
    duration_ms BIGINT NULL COMMENT '调用耗时，单位毫秒',
    started_at DATETIME NULL COMMENT '调用开始时间',
    finished_at DATETIME NULL COMMENT '调用结束时间',
    create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    create_by BIGINT NULL COMMENT '创建人 ID',
    update_by BIGINT NULL COMMENT '更新人 ID',
    deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除：0=正常，1=已删除',
    PRIMARY KEY (id),
    KEY idx_biz_ai_call_user_id (user_id),
    KEY idx_biz_ai_call_business (business_type, business_id),
    KEY idx_biz_ai_call_status (status),
    KEY idx_biz_ai_call_http_status (http_status),
    KEY idx_biz_ai_call_trace_id (trace_id),
    KEY idx_biz_ai_call_create_time (create_time)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='AI 调用日志表';
